#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	unsigned int	length;
	char			*str;

	i = 0;
	str = NULL;
	length = (unsigned int)(ft_strlen(s));
	if (!(str = (char *)malloc(sizeof(char) * (length + 1))))
		return (NULL);
	while (i < length)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
